#include <stdio.h>

int main(){
    int a = 37;
    int b = 14;
    printf("sum: %d\n", a + b);
    printf("product: %d\n", a * b);
    printf("difference: %d\n", a - b);
    printf("division: %f\n", ((float) a) /((float) b));
    printf("remainder: %d\n", a % b);
    printf("a: %d, b: %d\n", a, b);
}
